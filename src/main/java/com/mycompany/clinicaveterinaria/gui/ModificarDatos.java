    private void cargarDatos(int id_duenio) {
        this.masco = control.traerMascota(id_duenio);
        
        txtColor.setText(masco.getColor());
        txtDNI.setText(masco.getUnduenio().getDNI());
        txtDireccion.setText(masco.getUnduenio().getDireccion());
        txtNombreDuenio.setText(masco.getUnduenio().getNombreDuenio());
        txtEdad.setText(String.valueOf(masco.getEdad()));
        txtEspecie.setText(masco.getEspecie());
        txtID.setText(String.valueOf(masco.getid()));
        txtNombre.setText(masco.getUnduenio().getNombreDuenio());
        txtPeso.setText(String.valueOf(masco.getPeso()));
        txtRaza.setText(masco.getRaza());
        txtSexo.setText(masco.getSexo());
        txtTelefono.setText(masco.getUnduenio().getTelefono());
    }
